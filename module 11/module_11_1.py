import requests
# Отправка GET-запроса на сайт
response = requests.get('https://jsonplaceholder.typicode.com/posts')
data = response.json()  # Преобразование ответа в формат JSON
# Вывод первых 5 постов
for post in data[:5]:
    print(f"Title: {post['title']}\nBody: {post['body']}\n")

